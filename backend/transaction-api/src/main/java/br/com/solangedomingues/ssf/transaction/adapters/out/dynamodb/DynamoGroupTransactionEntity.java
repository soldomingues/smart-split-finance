package br.com.solangedomingues.ssf.transaction.adapters.out.dynamodb;

import br.com.solangedomingues.ssf.transaction.domain.models.UserShare;
import com.amazonaws.services.dynamodbv2.datamodeling.*;
import org.springframework.data.annotation.Id;

import java.math.BigDecimal;
import java.util.List;

@DynamoDBTable(tableName = "GroupTransactions")
public class DynamoGroupTransactionEntity {
    @Id
    private GroupTransactionId id;
    private String description;
    private BigDecimal totalAmount;
    private String category;
    private List<UserShare> shares;

    @DynamoDBHashKey
    public String getGroupId() {
        return id.getGroupId();
    }

    @DynamoDBRangeKey
    @DynamoDBAutoGeneratedKey
    public String getTransactionId() {
        return id.getTransactionId();
    }

    public void setTransactionId(String transactionId) {
        if (id == null) {
            id = new GroupTransactionId();
        }
        id.setTransactionId(transactionId);
    }

    public void setGroupId(String groupId) {
        if (id == null) {
            id = new GroupTransactionId();
        }
        id.setGroupId(groupId);
    }

    @DynamoDBAttribute
    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @DynamoDBAttribute
    public BigDecimal getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(BigDecimal totalAmount) {
        this.totalAmount = totalAmount;
    }

    @DynamoDBAttribute
    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    @DynamoDBAttribute
    public List<UserShare> getShares() {
        return shares;
    }

    public void setShares(List<UserShare> shares) {
        this.shares = shares;
    }
}
